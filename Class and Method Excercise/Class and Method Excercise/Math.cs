using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Class_and_Method_Excercise
{//A class which contains math operations which has two parameters of integers and return an integer.
    public class Math
    {
        //method for addition.
        public int Sum(int variable1, int variable2)
        {

            return variable1 + variable2;
        }
        //method for multiplication
        public int Multiply(int variable1, int variable2)
        {

            return variable1 * variable2;
        }
        //method for substraction
        public int Substract(int variable1, int variable2)
        {
            return variable1 - variable2;

        }

    }


}
